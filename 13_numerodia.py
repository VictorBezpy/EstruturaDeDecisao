#numerodia.py

n = int(
	input(
		'Digite o numero do dia: '
))

if n == 1:
	print(
		'\nHoje é Domingo!\n'
)
elif n == 2:
	print(
		'\nHoje é Segunda-feira!\n'
)
elif n == 3:
	print(
		'\nHoje é Terça-feira!\n'
)
elif n == 4:
	print(
		'\nHoje é Quartaça-feira!\n'
)
elif n == 5:
	print(
		'\nHoje é Quinta-feira!\n'
)
elif n == 6:
	print(
		'\nHoje é Sexta-feira!\n'
)
elif n == 7:
	print(
		'\nHoje é Sábado!\n'
)
else:
	print(
		'\nVALOR INCORRETO\n'
)
